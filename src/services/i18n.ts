export function t(key: keyof typeof translations['en'] | string, params?: Record<string, string>): string {
  const dict = translations[currentLang.value] || translations['en']
  let text = (dict as any)[key] || (translations['en'] as any)[key] || key
  if (params) {
    Object.keys(params).forEach((pk) => {
      text = text.replace(`{${pk}}`, params[pk])
    })
  }
  return text
}
